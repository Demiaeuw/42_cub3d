#include "../../include/cub.h"

void	message_error(char *str)
{
	write(2, "\n\033[31mError\033[0m\n\n", 18);
	write(2, str, ft_strlen(str));
	write(2, "\n\n", 2);
	exit(EXIT_FAILURE);
}